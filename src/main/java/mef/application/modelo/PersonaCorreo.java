package mef.application.modelo;

public class PersonaCorreo {
  
	private String desctipousuario;
	
	private String desctipopersona;
 
	private String desctipodocumento; 
	
	private String nrodocumento;
 
	private String nombrepersona;
  
	private String celular;
	
	private String telefono; 

	private String correo;
	
	private String correo_copia;
	
	private String fec_creacion;

	public String getDesctipousuario() {
		return desctipousuario;
	}

	public void setDesctipousuario(String desctipousuario) {
		this.desctipousuario = desctipousuario;
	}

	public String getDesctipopersona() {
		return desctipopersona;
	}

	public void setDesctipopersona(String desctipopersona) {
		this.desctipopersona = desctipopersona;
	}

	public String getDesctipodocumento() {
		return desctipodocumento;
	}

	public void setDesctipodocumento(String desctipodocumento) {
		this.desctipodocumento = desctipodocumento;
	}

	public String getNrodocumento() {
		return nrodocumento;
	}

	public void setNrodocumento(String nrodocumento) {
		this.nrodocumento = nrodocumento;
	}

	public String getNombrepersona() {
		return nombrepersona;
	}

	public void setNombrepersona(String nombrepersona) {
		this.nombrepersona = nombrepersona;
	}

	public String getCelular() {
		return celular;
	}

	public void setCelular(String celular) {
		this.celular = celular;
	}

	public String getTelefono() {
		return telefono;
	}

	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}

	public String getCorreo() {
		return correo;
	}

	public void setCorreo(String correo) {
		this.correo = correo;
	}

	public String getCorreo_copia() {
		return correo_copia;
	}

	public void setCorreo_copia(String correo_copia) {
		this.correo_copia = correo_copia;
	}

	public String getFec_creacion() {
		return fec_creacion;
	}

	public void setFec_creacion(String fec_creacion) {
		this.fec_creacion = fec_creacion;
	}
	
}
